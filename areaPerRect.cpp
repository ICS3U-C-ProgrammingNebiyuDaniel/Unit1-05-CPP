#include <iostream>

int main() {
    std::cout << "For a rectangle that has dimensions\n";
    std::cout << "length 9cm and width 2cm:\n";
    std::cout << "\n";
    std::cout << "Area = " << 9 * 2 << " cm^2\n";
    std::cout << "Perimeter = " << 2 * (9 + 2) << " cm\n";
}
